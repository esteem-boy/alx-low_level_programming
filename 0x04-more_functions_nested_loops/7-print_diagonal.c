#include "main.h"

/**
* print_diagonal - this func prints out a horizontal line on the terminal
*
* @n: it is the argument of the func
*
* Return: functions of this type do necessarily have return anything
*
*/
void print_diagonal(int n)
{
	int i;
	int j;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; j < n; j++)
			{
				if (j == i)
				{
					_putchar('\\');
				}
				else
				{
					_putchar(' ');
				}
			}
			_putchar('\n');
		}
	}
}
